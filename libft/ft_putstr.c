#include <unistd.h>
#include "libft.h"

void	ft_putstr(const char *c)
{
	int i;

	if (!c)
		return ;
	i = 0;
	while (c[i] != '\0')
	{
		ft_putchar(c[i]);
		i++;
	}
}
